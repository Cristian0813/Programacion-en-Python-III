# OPERADORES LÓGICOS EN PYTHON

# Los operadores lógicos se utilizan para combinar condiciones booleanas y evaluar
# expresiones lógicas. Aquí tienes los operadores lógicos más importantes:

# 1. AND (and): Devuelve True si ambas condiciones son verdaderas.
a = True
b = False
resultado = a and b
print(a, "and", b, "=", resultado)  # False.

# 2. OR (or): Devuelve True si al menos una de las condiciones es verdadera.
a = True
b = False
resultado = a or b
print(a, "or", b, "=", resultado) # True.

# 3. NOT (not): Invierte el valor de la condición.
a = True
resultado = not a
print("not", a, "=", resultado)  # False.

# Estos operadores son fundamentales para la ejecución de lógica condicional en Python.
