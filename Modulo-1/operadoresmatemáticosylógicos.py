## Operadores Matemáticos en Python

# Los operadores matemáticos se utilizan para realizar cálculos aritméticos
# entre valores. A continuación, te presento los operadores matemáticos más comunes en Python:

# 1. Suma (+): Realiza la adición entre los operandos.
a = 5
b = 3
resultado = 5+3
print(a, "+", b, "=", resultado) # Imprime 8

# 2. Resta (-): Realiza la substracción entre los operandos.
a = 12
b = 3
resultado = 12-3
print(a, "-", b, "=", resultado) # Imprime 9

# 3. Multiplicación (*): Realiza la multiplicación entre los operandos.
a = 12
b = 3
resultado = 12*3
print(a, "*", b, "=", resultado) # Imprime 36

# 4. División (/): Realiza la división entre los operandos.
a = 12
b = 3
resultado = 12/3
print(a, "/", b, "=", resultado) # Imprime 4

# 5. Módulo (%): Calcula el residuo de la división entre los operandos.
a = 12
b = 3
resultado = 12%3
print(a, "%", b, "=", resultado) # Imprime 1

# 6. Potencia (**): Calcula la potencia de los operandos.
a = 12
b = 3
resultado = 12**3
print(a, "**", b, "=", resultado) # Imprime 1728

# 7. División con resultado de número entero (//): Realiza la división y devuelve el resultado como número entero.
a = 18
b = 5
resultado = 18//5
print(a, "//", b, "=", resultado) # Imprime 3

# Nota: Para obtener el resultado en tipo flotante, uno de los operandos también debe ser de tipo flotante.

