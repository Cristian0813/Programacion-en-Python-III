# FUNCIONES INTEGRADAS

# También conocidas como funciones built-in son funciones predefinidas en el lenguaje y se pueden utilizar directamente sin necesidad de importar módulos adicionales. Estas funciones están disponibles para su uso en cualquier parte del código y proporcionan funcionalidades básicas y comunes. A continuación, te presento algunas de las funciones integradas más utilizadas:

# 1. print(): Utilizada para imprimir en la consola o terminal. Por ejemplo:
print("Hola, mundo")

# 2. len(): Retorna la longitud de un objeto iterable (como una lista, tupla o cadena). Por ejemplo:
nombre = "Python"
longitud = len(nombre)  # Devuelve 6

# 3. input(): Permite la entrada de datos por parte del usuario. Por defecto, los datos ingresados se tratan como cadenas (strings). Por ejemplo:
nombre = input("¿Cuál es tu nombre? ")
print("¡Hola,", nombre, "!")  # Salida: ¡Hola, [nombre]!

# 4. range(): Genera una secuencia de números. Por ejemplo:
numeros = range(1, 6)  # Genera los números del 1 al 5

# 5. type(): Devuelve el tipo de un objeto. Por ejemplo:
numero = 42
tipo = type(numero)  # Devuelve <class 'int'>
