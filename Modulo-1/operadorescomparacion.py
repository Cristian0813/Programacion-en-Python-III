# OPERADORES DE COMPARACIÓN

# Los operadores de comparación nos permiten evaluar relaciones entre dos
# valores y determinar si son iguales, diferentes, mayores, menores, mayores/iguales
# o menores/iguales. Aquí tienes algunos ejemplos:

# 1. Igualdad (`==`): Verifica si dos valores son iguales.
a = 5
b = 5
es_igual = (a == b)  # True
print(es_igual)

# 2. Desigualdad (`!=`): Verifica si dos valores no son iguales.

a = 5
b = 3
no_es_igual = (a != b)  # True
print(no_es_igual)

# 3. Mayor que (`>`): Verifica si un valor es mayor que otro.

a = 10
b = 5
es_mayor = (a > b)  # True
print(es_mayor)

# 4. Menor que (`<`): Verifica si un valor es menor que otro.

a = 3
b = 7
es_menor = (a < b)  # True
print(es_menor)

# 5. Mayor o igual que (`>=`): Verifica si un valor es mayor o igual que otro.

a = 10
b = 10
es_mayor_igual = (a >= b)  # True
print(es_mayor_igual)

# 6. Menor o igual que (`<=`): Verifica si un valor es menor o igual que otro.

a = 5
b = 7
es_menor_igual = (a <= b)  # True
print(es_menor_igual)

#  Estos operadores son fundamentales para la lógica de control de flujo en Python. Puedes utilizarlos en expresiones condicionales, bucles y más.

